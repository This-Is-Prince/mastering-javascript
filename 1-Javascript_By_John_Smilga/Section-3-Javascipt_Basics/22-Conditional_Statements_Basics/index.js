// Conditional Statements
// >, <, >=, <=, ==, ===, !=,!==

if (true) {
  console.log("hello world");
}

if (2 > 1) {
  console.log("2 is bigger than 1");
}

const value = true;
if (value) {
  console.log("value is " + value);
} else {
  console.log("value is " + value);
}

const cond = 2 > 1;
if (cond) {
  console.log("value of cond is " + cond);
}

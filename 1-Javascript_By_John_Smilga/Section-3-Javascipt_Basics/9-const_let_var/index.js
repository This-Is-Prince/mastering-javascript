// LET VS CONST VS VAR

// using var
var value = "some value";

// using let
let name = "john";
name = "peter";

// using const (CONSTANT) - can't re-assign
const lastName = "jordan";

console.log("value = ", value);
console.log(name);
console.log(lastName);

value = "some different value";
console.log("value = ", value);
